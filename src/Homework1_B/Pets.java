package Homework1_B;
//Pet class and attributes.
public class Pets {
    public String name;
    public String type;
//Pet constructor.
    public Pets(String name, String type) {
        this.name = name;
        this.type = type;

    }
//Getters for our main function.
    public String getName() {
        return name;
    }

    public String getType() {
        return type;

    }

}


